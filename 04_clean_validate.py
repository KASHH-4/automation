def clean_supplier_name(value):
    """
    Normalize supplier name using a known-variant map first;
    fall back to simple title-casing for anything not in the map.
    """
    if not value:
        return None
    key = re.sub(r"[.\-]", "", str(value)).strip().lower()
    key = re.sub(r"\s+", " ", key)
    if key in SUPPLIER_NAME_MAP:
        return SUPPLIER_NAME_MAP[key]
    return str(value).strip().title()


MONTHS = {
    "jan": "01", "feb": "02", "mar": "03", "apr": "04", "may": "05", "jun": "06",
    "jul": "07", "aug": "08", "sep": "09", "oct": "10", "nov": "11", "dec": "12",
}


def clean_date(value):
    """
    Normalize common date formats into ISO 'YYYY-MM-DD'.
    Handles: '20 Jul 2026', '20/07/26', '2026-07-20', '20-07-2026'
    Returns None if it cannot be confidently parsed.
    """
    if not value:
        return None
    text = str(value).strip()

    # Already ISO
    m = re.match(r"^(\d{4})-(\d{2})-(\d{2})$", text)
    if m:
        return text

    # '20 Jul 2026' or '20 July 2026'
    m = re.match(r"^(\d{1,2})\s+([A-Za-z]{3,9})\s+(\d{4})$", text)
    if m:
        day, month_name, year = m.groups()
        month = MONTHS.get(month_name[:3].lower())
        if month:
            return f"{year}-{month}-{int(day):02d}"

    # '20/07/26' or '20-07-2026' or '20/07/2026' (assume DD/MM/YY[YY])
    m = re.match(r"^(\d{1,2})[/\-](\d{1,2})[/\-](\d{2,4})$", text)
    if m:
        day, month, year = m.groups()
        if len(year) == 2:
            year = "20" + year
        return f"{year}-{int(month):02d}-{int(day):02d}"

    return None  # unparseable — flagged during validation instead of guessed


def normalize_currency_code(value):
    if not value:
        return None
    code = str(value).strip().upper()
    return code if re.match(r"^[A-Z]{3}$", code) else None


