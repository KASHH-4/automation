def clean_price(value):
    """
    Normalize a messy price string/number into a float.
    Handles: '₹820', '820 INR', 'Rs.820', 'INR 45,000', 45000, '45,000.50'
    Returns None if it cannot be parsed.
    """
    if value is None:
        return None
    if isinstance(value, (int, float)):
        return float(value)

    text = str(value).strip()
    # Strip currency symbols/codes and thousands separators, keep digits and decimal point
    text = re.sub(r"[₹$€]", "", text)
    text = re.sub(r"\bRs\.?\b", "", text, flags=re.IGNORECASE)
    text = re.sub(r"\b[A-Za-z]{3}\b", "", text)  # drop 3-letter currency codes like INR/USD
    text = text.replace(",", "").strip()
    text = re.sub(r"[^\d\.]", "", text)  # drop anything left that isn't a digit or dot

    if not text:
        return None
    try:
        return float(text)
    except ValueError:
        return None


def clean_percentage(value):
    """Normalize a GST/percentage string like '18%' or '18' into a float (18.0)."""
    if value is None:
        return None
    if isinstance(value, (int, float)):
        return float(value)
    text = re.sub(r"[^\d\.]", "", str(value))
    if not text:
        return None
    try:
        return float(text)
    except ValueError:
        return None


# Known supplier name variants -> canonical name.
