supplier_clean = []
supplier_invalid = []

for i, row in enumerate(supplier_raw):
    supplier_name = clean_supplier_name(row.get("Supplier"))
    currency = normalize_currency_code(row.get("Currency"))
    price = clean_price(row.get("Unit Price"))

    errors = []
    if not supplier_name:
        errors.append("missing supplier")
    if not currency:
        errors.append("invalid/missing currency")
    if price is None or price <= 0:
        errors.append("price not positive")

    if errors:
        supplier_invalid.append({"row_index": i, "errors": errors, "raw": row})
        continue

    supplier_clean.append({
        "material": str(row.get("Material", "")).strip(),
        "specification": str(row.get("Specification", "")).strip(),
        "supplier": supplier_name,
        "unit_price": price,
        "currency": currency,
        "unit": str(row.get("Unit", "")).strip(),
        "lead_time": row.get("Lead Time"),
        "moq": row.get("MOQ"),
        "available_stock": row.get("Available Stock"),
    })

print(f"✅ {len(supplier_clean)} valid rows, ⚠️ {len(supplier_invalid)} invalid rows")

